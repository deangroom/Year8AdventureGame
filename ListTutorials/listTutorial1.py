# Creating a list
my_list = [1, 2, 3, 4, 5]

# Accessing elements in a list
print(my_list[0])  # Output: 1

# Modifying elements in a list
my_list[1] = 10
print(my_list)  # Output: [1, 10, 3, 4, 5]

# Length of a list
print(len(my_list))  # Output: 5

# Basic list methods
my_list.append(6)
print(my_list)  # Output: [1, 10, 3, 4, 5, 6]

# Iterating through a list
for item in my_list:
    print(item)
